DOSSEG            ;DOS SEGMENT
.MODEL SMALL      ;MODEL DIRECTIVE
.STACK 100H       ;STACK SEGMENT DIRECTIVE
.DATA             ;VARIABLES ARE DECLARED HERE
QUO DB ?          ;VARIABLE FOR QUOTIENT
REM DB ?          ;VARIABLE FOR REMAINDER
.CODE             ;EXCETUABLE INSTRUCTIONS ARE WRITTEN HERE
MAIN PROC         ;MAIN PROCEDURE
MOV AX,21         ;DIVEDEND GOES TO AX,21
MOV BL,5          ;DIVISOR GO TO BL
DIV BL            ;COMMAND FOR DIVISION
MOV QUO,AL        ;MOVE QUOTIENT AL TO QUO
MOV REM,AH        ;MOVE REMAINDER AH TO REM
MOV DL,QUO        ;MOVE QUO TO DL
ADD DL,48         ;TO CONVERT NUMBER INTO ASCII CODE ADDING 48 TO DL
MOV AH,2          ;SERVICE ROUTINE 2 IS USED TO PRINT DATA
INT 21H
MOV DL,REM        ;MOVE REM TO DL
ADD DL,48         ;TO CONVERT NUMBER INTO ASCII CODE ADDING 48 TO DL
MOV AH,2
INT 21H
MOV AH,4CH       ;TO EXIT THE REGISTER
INT 21H
MAIN ENDP         
END MAIN          ;TO END MAIN PROGRAM
