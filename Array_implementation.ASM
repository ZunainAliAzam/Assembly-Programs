;PROGRAM TO PRINT ARRAY USING LOOP
DOSSEG                 ;DOS SEGMENT
.MODEL SMALL           ;MODEL DIRECTIVE
.STACK 100H            ;STACK SEGMENT DIRECTIVE
.DATA
ARRAY DB 'Z','U','N','A','I','N'      ;INITIALIZE CHARACTERS IN ARRAY
.CODE                  ;EXECUTABLE INSTRUCTION ARE WRITTEN HERE
MAIN PROC              ;MAIN PROCEDURE
MOV AX,@DATA           ;TO ACCESS VARIABLE IN .DATA
MOV DS,AX              
MOV SI,OFFSET ARRAY    ;ADDRESS OF ARRAY GOES TO SI
MOV CX,6               ;LOOPS RUNS 6 TIMES
L1:
MOV DX,[SI]            ;MOV ADDRESS OF SI TO DX
MOV AH,2               ;USING 2 AS SERVICE ROUTINE TO PRINT DATA
INT 21H
INC SI                 ;INCREMENT IN SI
LOOP L1                ;REPEAT LOOP
MOV AH,4CH             ;TO EXIT THE REGISTER
INT 21H
MAIN ENDP              ;END PROCEDURE
END MAIN               ;END PROGRAM  
