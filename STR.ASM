;PROGRAM TO INPUT STRING AND PRINT IT
DOSSEG                  ;DOS SEGMENT
.MODEL SMALL            ;MODEL DIRECTIVE
.STACK 100H             ;STACK SEGMENT DIRECTIVE
.DATA
ARR1 DB 100 DUP('$')    ;100 CHARACTER LIMIT FOR USER INPUT
.CODE                   ;EXECUTABLE INSTRUCTIONS ARE WRITTEN HERE
MAIN PROC               ;MAIN PROCEDURE

MOV AX,@DATA            ;ACCESS VARIABLE IN .DATA
MOV DS,AX               ;HEAP MEMORY INITIALIZE
MOV SI,OFFSET ARR1      ;TO ACCESS ARRAY, STRING ADDRESS OF ARR1, GO TO SI

L1:
MOV AH,1                ;TO TAKE INPUT
INT 21H
CMP AL,13               ;COMPARE THE INPUT TO  THE ASCII CODE OF INPUT KEY
JE PROGRAMEND           ;JUMP IS EWUAL TO PROGRAM END
MOV [SI],AL
INC SI                  ;INCREMENT IN SI
JMP L1                  ;AGAIN JUMP TO LABEL L1 TO TAKE INPUT
PROGRAMEND:
MOV DX,OFFSET ARR1      ;INITIALIZE ARRAY
MOV AH,9                ;USING 9 AS SERVICE ROUTINE TO PRINT STRING
INT 21H

MOV AH,4CH              ;TO EXIT REGISTER
INT 21H
MAIN ENDP               ;END PROCEDURE
END MAIN                ;END PROGRAM
