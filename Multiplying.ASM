DOSSEG              ;DOS SEGMENT
.MODEL SMALL        ;MODEL DIRECTIVE
.STACK 100H         ;STACK DIRECTIVE
.DATA               ;INSIDE THIS VARIABLES ARE STORED
.CODE               ;EXECUTABLE INTRUCTIONS ARE WRITTEN HERE
MAIN PROC           ;MAIN PROCEDURE
MOV AL,5            ;MOVE 5 TO REGISTER AL
MOV BL,9            ;MOVE 9 TO REGISTER BL
MUL BL              ;COMMAND FOR MULTIPLICATION
AAM
MOV CH,AH           ;MOVE AH TO CH
MOV CL,AL           ;MOVE AL TO CL
MOV DL,CH           ;MOVE CH TO DL
ADD DL,48           ;TO CONVERT NUMBER IN ASCII CODE ADDING 48 TO DL 
MOV AH,2            ;SERVICE ROUTINE 2 IS USED TO PRINT DATA
INT 21H

MOV DL,CL           ;MOVE CL TO DL
ADD DL,48           ;TO CONVERT NUMBER IN ASCII CODE ADDING 48 TO DL
MOV AH,2            ;SERVICE ROUTINE 2 IS USED TO PRINT DATA
INT 21H
MOV AH,4CH          ;TO EXIT REGISTER
INT 21H

MAIN ENDP           ;END MAIN PROGRAM
END MAIN            
